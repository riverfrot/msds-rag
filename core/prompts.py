def get_system_prompt(section_number: int) -> str:
    if section_number not in _SECTION_FORMATS:
        raise ValueError(
            f"MSDS section must be 1..16, got: {section_number}"
        )
    return _BASE + "\n출력 형식:\n" + _SECTION_FORMATS[section_number]


# Backward-compatible alias used in document/ClovaClient.md.
SYSTEM_PROMPTS = {n: get_system_prompt(n) for n in _SECTION_FORMATS}
